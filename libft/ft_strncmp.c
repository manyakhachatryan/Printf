#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int	r;

	r = 0;
	while ((*s1 || *s2) && r == 0 && n > 0)
	{
		if (*s1 != *s2)
			r = *s1 - *s2;
		else
		{
			s1++;
			s2++;
			n--;
		}
	}
	return (r);
}
